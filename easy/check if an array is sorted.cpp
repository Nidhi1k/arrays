
class Solution
{
public:
    int largest(vector<int> &arr, int n)
    {
    int max = arr[0];
    for(int it=0;it<n;it++)
    {
        if(arr[it]>max)
        max=arr[it];
    }
    return max;
     //t->0(n) s->0(1)
    //  sort(arr.begin(),arr.end());
    //  return arr[n-1];
    //t->0(nlogn) s->0(n)
    }
};
